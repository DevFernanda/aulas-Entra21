
function some(n1:number, n2: number): number {
    return n1 + n2
}

function subtraia(n3:number, n4:number): number{
    return n3 - n4
}

function multiplique(n1:number, n2:number): number {
    return (n1 * n2)
}
function divida(n1:number, n2:number): number {
    return(n1 / n2)
}

export {some, subtraia, multiplique, divida}